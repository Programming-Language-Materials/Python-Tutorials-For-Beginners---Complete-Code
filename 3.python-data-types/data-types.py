# Data Types

# String
first_name = "Alex"
print(first_name)

# Integer
number_integer = 20
print(number_integer)

# Float
number_float = 20.2245
print(number_float)

# List
games_list = ["Counter Strike",
"Half Life","Call of Duty"]
print(games_list)

# Dictionary
student_dict = {
  "name" : "John",
  "age" : 23
}
print(student_dict)

print(type(games_list))
print(type(student_dict))
